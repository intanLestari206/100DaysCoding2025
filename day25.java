public class day25 {
    public static void main(String[] args) {
        int x = 5;
        x++; // post-increment
        System.out.println( x);
        x--;// post-Decrement
        System.out.println(x);
       
        int a = 5;
        ++a; // pre-increment
        System.out.println(a);
        --a;// pre-decrement
        System.out.println(a);

    }
}
